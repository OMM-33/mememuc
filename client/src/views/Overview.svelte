<script>
	import { memes, updateMemes } from "../cache";

	import Button from "../lib/Button.svelte";
	import Graph from "../lib/Graph.svelte";
	import Frame from "../lib/View/Frame.svelte";
	import InfiniteScroll from "svelte-infinite-scroll";

	updateMemes();


	let memesArray = [];
	$: {
		const result = [];
		const memeStores = [...$memes.values()];
		memeStores.forEach((store, i) => store.subscribe(value => result[i] = value));
		memesArray = result;
	}

	$: publicMemes = memesArray.filter(({ privacy }) => privacy === "public");

	$: stats = Object.fromEntries(
		Object.entries(
			publicMemes.reduce((result, meme) => {
				const date = meme.updateDate;
				const dateStr = `${date.getFullYear()}-${String(date.getMonth() + 1).padStart(2, "0")}`;
				if (!result[dateStr]) result[dateStr] = 0;
				result[dateStr]++;
				return result;
			}, {}),
		)
			.sort(([dateA], [dateB]) => dateA < dateB ? -1 : 1)
			.map(([date, count]) => {
				const [year, month] = date.split("-");
				return [`${month}/${year}`, count];
			}),
	);

	let statsVisible = false;

	function sortBy(criterium){
		console.log("Sort by: " + criterium);
	}
	function filter(){
		const sb = document.getElementById("dropdown").selectedIndex;
		console.log("Filter: " + sb);
	}

	let newBatch = [];

	function fetchData() {
		newBatch++;
		console.log(newBatch);
	}
</script>

<h1>Overview </h1>

<div class="top-controls">
	<div class="sort"><p style="margin-right: 1em">Sort by:</p><Button on:click={()=>sortBy("date")}>Date</Button><Button on:click={()=>sortBy("title")}>Title</Button></div>
	<div class="filter"> <p style="margin-right: 1em">more than</p> <select id="dropdown">
		<option value="1">10 views</option>
		<option value="2">20 views</option>
		<option value="3">10 upvotes</option>
		<option value="4">20 upvotes</option>
	</select> <Button on:click={filter}>Filter</Button>	</div>
	<Button style="margin-left: auto" on:click={() => statsVisible = !statsVisible}>
		ðŸ“‰ {statsVisible ? "Hide" : "Show"} Statistics
	</Button>
</div>
{#if statsVisible}
	<div class="graph">
		<Graph
			type="line"
			labels={Object.keys(stats)}
			datasets={[{
				label: "Memes created",
				data: Object.values(stats),
				backgroundColor: "#26ba8955",
				borderColor: "#26ba89",
			}]}
			options={{
				scale: {
					ticks: {
						precision: 0,
					},
				},
			}}
		/>
	</div>
{/if}
<div class="grid">
	{#each publicMemes as meme}
		<Frame {meme} />
	{/each}
</div>
<InfiniteScroll
	hasMore={newBatch.length}
	threshold={10}
	on:loadMore={() => {fetchData;}} />

